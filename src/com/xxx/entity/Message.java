package com.xxx.entity;

public class Message {
	private int mesid;
	private int uid;
	private String mess_content;
	private int mess_status;
	public int getMesid() {
		return mesid;
	}
	public void setMesid(int mesid) {
		this.mesid = mesid;
	}
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public String getMess_content() {
		return mess_content;
	}
	public void setMess_content(String mess_content) {
		this.mess_content = mess_content;
	}
	public int getMess_status() {
		return mess_status;
	}
	public void setMess_status(int mess_status) {
		this.mess_status = mess_status;
	}
	

}
